   private void drawGrid()
   {
      Graphics g = grid.getGraphics();
      int panel_width = grid.getWidth();
      int panel_height = grid.getHeight();
      int step_x = panel_width / 10;
      int step_y = panel_height / 10;

      for (int i = 1; i < 10; i ++)
      {
         g.drawLine(i * step_x, 0, i * step_x, panel_height);
         g.drawLine(0, i * step_y, panel_width, i * step_y);
      }
   }
   
   private void checkBuildings()
   {
       if(building5.isVisible()&&building4.isVisible()&&building3A.isVisible()&&building3B.isVisible()&&building2.isVisible())
           continueButton.setEnabled(true);
       else
           continueButton.setEnabled(false);
   }
  
   @Override
   public void paint(Graphics g)
   {
      super.paint(g);
      drawGrid();
   }
    
