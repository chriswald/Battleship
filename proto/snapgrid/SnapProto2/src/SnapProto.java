    private void gridMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_gridMousePressed
        int x = (evt.getX()/20)*20;
        int y = (evt.getY()/20)*20;
        
        if(radio5.isSelected())
        {
            building5.setLocation(x, y);
            if(!building5.isVisible())
                building5.setVisible(true);
        }
        if(radio4.isSelected())
        {
            building4.setLocation(x, y);
            if(!building4.isVisible())
                building4.setVisible(true);
        }
        if(radio3A.isSelected())
        {
            building3A.setLocation(x, y);
            if(!building3A.isVisible())
                building3A.setVisible(true);
        }
        if(radio3B.isSelected())
        {
            building3B.setLocation(x, y);
            if(!building3B.isVisible())
                building3B.setVisible(true);
        }
        if(radio2.isSelected())
        {
            building2.setLocation(x, y);
            if(!building2.isVisible())
                building2.setVisible(true);
        }
        
        checkBuildings();
        this.repaint();
    }//GEN-LAST:event_gridMousePressed

    private void rotateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rotateButtonActionPerformed
        if(radio5.isSelected())
            building5.setSize(building5.getHeight(), building5.getWidth());
        else if(radio4.isSelected())
            building4.setSize(building4.getHeight(), building4.getWidth());
        else if(radio3A.isSelected())
            building3A.setSize(building3A.getHeight(), building3A.getWidth());
        else if(radio3B.isSelected())
            building3B.setSize(building3B.getHeight(), building3B.getWidth());
        else if(radio2.isSelected())
            building2.setSize(building2.getHeight(), building2.getWidth());
        else
            return;
        
        this.repaint();
    }//GEN-LAST:event_rotateButtonActionPerformed

